    void init() {
        setIconImage(XImage.getAppIcon());
        setTitle("EduSys-Quản lý nhân viên");
        setLocationRelativeTo(null);
        fillTable();
        updateStatus();
    }

    void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);//xóa tất cả các hàng trên JTable
        try {
            List<NhanVien> list = dao.selectAll();//đọc dữ liệu từ csdl
            for (NhanVien nv : list) {
                Object[] row = {nv.getMaNV(), nv.getMatKhau(), nv.getHoTen(), nv.isVaiTro() ? "Trưởng Phòng" : "Nhân Viên"
                };
                model.addRow(row);//thêm 1 hàng vào JTable
            }
        } catch (Exception e) {
            MsgBox.alter(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void edit() {
        try {
            String MaNV = (String) tblNhanVien.getValueAt(this.row, 0);
            NhanVien model = dao.selectById(MaNV);
            if (model != null) {
                setForm(model);
                updateStatus();
                tabs.setSelectedIndex(0);
            }
        } catch (Exception e) {
            MsgBox.alter(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    void setForm(NhanVien model) {
        txtMaNV.setText(model.getMaNV());
        txtHoTen.setText(model.getHoTen());
        txtMK.setText(model.getMatKhau());
        txtXNMK.setText(model.getMatKhau());
        rboTruongPhong.setSelected(model.isVaiTro());
        rboNhanVien.setSelected(model.isVaiTro());
    }

    NhanVien getForm() {
        NhanVien model = new NhanVien();
        model.setMaNV(txtMaNV.getText());
        model.setHoTen(txtHoTen.getText());
        model.setMatKhau(new String(txtMK.getPassword()));
        model.setVaiTro(rboTruongPhong.isSelected());
        return model;
    }

    void updateStatus() {
        boolean edit = this.row >= 0;
        boolean first = this.row == 0;
        boolean last = this.row == tblNhanVien.getRowCount() - 1;
        txtMaNV.setEditable(!edit);
        //khi insert thì ko update, delete
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPre.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    void clearForm() {
        NhanVien nv = new NhanVien();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
    }

    void insert() {
        NhanVien model = getForm();
        String confirm = new String(txtXNMK.getPassword());
        if (confirm.equals(model.getMatKhau())) {
            try {
                dao.insert(model);
                this.fillTable();
                this.clearForm();
                MsgBox.alter(this, "Thêm mới thành công!");
            } catch (Exception e) {
                MsgBox.alter(this, "Thêm mới thất bại!");
            }
        } else {
            MsgBox.alter(this, "Xác nhận mật khẩu không đúng!");
        }
    }

    void update() {
        NhanVien model = getForm();
        String confirm = new String(txtXNMK.getPassword());
        if (confirm.equals(model.getMatKhau())) {
            MsgBox.alter(this, "Xác nhận mật khẩu không đúng!");
        } else {
            try {
                dao.update(model);
                this.fillTable();
                MsgBox.alter(this, "Cập nhật thành công!");
            } catch (Exception e) {
                MsgBox.alter(this, "Cập nhật thất bại!");
            }
        }
    }

    void delete() {
        if (Auth.isManager()) {
            MsgBox.alter(this, "Bạn không có quyền xóa nhân viên!");
        } else {
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa nhân viên này?")) {
                String MaNV = txtMaNV.getText();
                try {
                    dao.delete(MaNV);
                    this.fillTable();
                    this.clearForm();
                    MsgBox.alter(this, "Xóa thành công!");
                } catch (Exception e) {
                    MsgBox.alter(this, "Xóa thất bại!");
                }
            }
        }
    }

    void First() {
        row = 0;
        edit();
    }

    void Pre() {
        if (row > 0) {
            row--;
            edit();
        }
    }

    void Next() {
        if (row < tblNhanVien.getRowCount() - 1) {
            row++;
            edit();
        }
    }

    void Last() {
        row = tblNhanVien.getRowCount() - 1;
        edit();
    }

